// ValidateIconData は、アイコンデータが有効な形式かどうかを検証します。
func ValidateIconData(data []byte) error {
	if len(data) == 0 {
		return fmt.Errorf("アイコンデータが空です")
	}

	if runtime.GOOS == "windows" {
		// ICOマジックナンバーのチェック (00 00 01 00)
		icoMagic := []byte{0x00, 0x00, 0x01, 0x00}
		if len(data) < len(icoMagic) {
			return fmt.Errorf("アイコンデータが短すぎます (size=%d bytes)", len(data))
		}
		for i, b := range icoMagic {
			if data[i] != b {
				return fmt.Errorf("ICOマジックナンバーが不正です (offset=%d, expected=0x%02X, got=0x%02X)", i, b, data[i])
			}
		}
	} else {
		// PNGマジックナンバーのチェック (89 50 4E 47 0D 0A 1A 0A)
		pngMagic := []byte{0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a}
		if len(data) < len(pngMagic) {
			return fmt.Errorf("アイコンデータが短すぎます (size=%d bytes)", len(data))
		}
		for i, b := range pngMagic {
			if data[i] != b {
				return fmt.Errorf("PNGマジックナンバーが不正です (offset=%d, expected=0x%02X, got=0x%02X)", i, b, data[i])
			}
		}
	}

	return nil
}

// GetIconInfo は、デバッグ用にアイコンの詳細情報を返します。
func GetIconInfo(state string) string {
	data := GetIconData(state)
	if err := ValidateIconData(data); err != nil {
		return fmt.Sprintf("state=%s, error=%v", state, err)
	}

	format := "PNG"
	if runtime.GOOS == "windows" {
		format = "ICO"
	}

	return fmt.Sprintf("state=%s, size=%d bytes, format=%s, valid=true", state, len(data), format)
}
