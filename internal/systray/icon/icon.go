// GetIconData は、現在のOSに適したアイコンデータを返します。
func GetIconData(state string) []byte {
	if runtime.GOOS == "windows" {
		return getIconDataICO(state)
	}
	return getIconDataPNG(state)
}

// getIconDataICO は、ICO形式のアイコンデータを返します（Windows用）。
